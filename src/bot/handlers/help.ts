import { Context } from 'telegraf';

export async function handleHelp(ctx: Context) {
  const helpMessage = `*Подробная инструкция по использованию*

*Формат запроса:*
Отправьте текстовое сообщение с темой для исследования.

*Примеры запросов:*
• "Климатические изменения в 2024 году"
• "Проверь информацию о новой вакцине от COVID"
• "Последние новости о выборах в США"

*Процесс работы:*
1. Вы отправляете запрос
2. Бот ищет информацию в доверенных источниках
3. Проводит кросс-чек найденных фактов
4. Выделяет ключевые цитаты
5. Переводит при необходимости
6. Создает структурированный отчет

*Доверенные источники:*
• Международные новостные агентства (BBC, Reuters, AP)
• Факт-чекинговые организации (Snopes, FactCheck.org)
• Академические публикации
• Официальные правительственные сайты

*Советы:*
• Формулируйте запросы конкретно
• Указывайте временной период, если это важно
• Можете задавать уточняющие вопросы

Нужна помощь? Напишите администратору: @your_admin`;

  await ctx.replyWithMarkdown(helpMessage);
}